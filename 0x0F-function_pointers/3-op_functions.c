#include "function_pointers.h"

/**
 * op_add - adds 2 int
 * @a: param
 * @b: param
 * Return: returns int
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub of 2 int
 * @a: param
 * @b: param
 * Return: returns int
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - product of  2 int
 * @a: param
 * @b: param
 * Return: returns int
 */

int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - divides 2 int
 * @a: param
 * @b: param
 * Return: returns int
 */


int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - reminder of 2 int
 * @a: param
 * @b: param
 * Return: returns int
 */

int op_mod(int a, int b)
{
	return (a % b);
}

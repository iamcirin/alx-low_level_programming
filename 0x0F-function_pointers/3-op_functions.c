#include "3-calc.h"

/**
 * op_add - a function that add 2 int
 * @a: 1st int
 * @b: 2nd int
 * Return: returns an int
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - a function that substract 2 ints
 * @a: 1st int
 * @b: 2nd int
 * Return: return int
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - a function that multiply 2 iny
 * @a: 1st int
 * @b: 2nd int
 * Return: returns an int
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - a function that divide 2 int
 * @a: 1st int
 * @b: 2nd int
 * Return: returns an int
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - a function that gives the reminder of the div of 2 int
 * @a: 1st int
 * @b: 2nd int
 * Return: returns an int
 */

int op_mod(int a, int b)
{
	return (a % b);
}
